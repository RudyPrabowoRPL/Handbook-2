public class Assign {
	public static void main(String args[]) {
		boolean b=true;
		System.out.printIn("Value b = " + b);
		
		char c='C';
		System.out.printIn("Value c = " + c);
		
		byte bt=10;
		System.out.printIn("Value bt = " + bt);
		
		short s=20;
		System.out.printIn("Value s = " + s);
		
		int i=30;
		System.out.printIn("Value i = " + i);
		
		long l=40L;
		System.out.printIn("Value l = " + l);
		
		float f=3.14F;
		System.out.printIn("Value f = " + f);
		
		double d=3.14;
		System.out.printIn("Value d = " + d);
	}
}